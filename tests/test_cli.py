class CliParsingTests(unittest.TestCase):
    def test_normalize_netmask_from_hex(self):
        dotted, prefix = normalize_netmask("0xfffffe00")
        self.assertEqual(dotted, "255.255.254.0")
        self.assertEqual(prefix, "23")

    def test_parse_ifconfig_extracts_mac_and_ipv4(self):
        interfaces = parse_ifconfig(IFCONFIG_SAMPLE)
        self.assertIn("en0", interfaces)
        self.assertEqual(interfaces["en0"]["mac"], "5a:41:83:8a:50:40")
        self.assertEqual(interfaces["en0"]["status"], "active")
        self.assertEqual(interfaces["en0"]["ipv4"][0]["address"], "172.17.6.246")

    def test_parse_default_route(self):
        route = parse_default_route(DEFAULT_ROUTE_SAMPLE)
        self.assertEqual(route["interface"], "en0")
        self.assertEqual(route["gateway"], "172.17.6.1")

    def test_parse_system_profiler(self):
        metadata = parse_system_profiler(SYSTEM_PROFILER_SAMPLE)
        self.assertEqual(metadata["en0"]["kind"], "Wi-Fi")
        self.assertEqual(metadata["en0"]["hardware_mac"], "bc:d0:74:37:9b:99")
        self.assertEqual(metadata["en0"]["dns"], ["223.5.5.5", "119.29.29.29"])

    def test_parse_ip_addr_json_extracts_linux_interfaces(self):
        interfaces = parse_ip_addr_json(LINUX_IP_ADDR_JSON)
        self.assertIn("enp0s31f6", interfaces)
        self.assertEqual(interfaces["enp0s31f6"]["mac"], "52:54:00:12:34:56")
        self.assertEqual(interfaces["enp0s31f6"]["ipv4"][0]["address"], "192.168.122.10")
        self.assertIn("veth1234", interfaces)

    def test_parse_ip_addr_output_fallback_extracts_linux_interfaces(self):
        interfaces = parse_ip_addr_output(LINUX_IP_ADDR_TEXT)
        self.assertEqual(interfaces["eth0"]["ipv4"][0]["prefix"], "24")
        self.assertEqual(interfaces["wg0"]["ipv4"][0]["address"], "10.8.0.2")

    def test_parse_ip_route_json(self):
        route = parse_ip_route_json(LINUX_ROUTE_JSON)
        self.assertEqual(route["interface"], "enp0s31f6")
        self.assertEqual(route["gateway"], "192.168.122.1")

    def test_parse_ip_route_output_fallback(self):
        route = parse_ip_route_output(LINUX_ROUTE_TEXT)
        self.assertEqual(route["interface"], "eth0")
        self.assertEqual(route["gateway"], "10.0.2.2")

    def test_parse_linux_resolv_conf(self):
        dns = parse_linux_resolv_conf(LINUX_RESOLV_CONF)
        self.assertEqual(dns, ["1.1.1.1", "8.8.8.8"])

    def test_parse_ipconfig_all_extracts_windows_interfaces(self):
        interfaces, metadata = parse_ipconfig_all(WINDOWS_IPCONFIG_SAMPLE)
        self.assertIn("Ethernet", interfaces)
        self.assertEqual(interfaces["Ethernet"]["mac"], "aa:bb:cc:dd:ee:ff")
        self.assertEqual(interfaces["Ethernet"]["ipv4"][0]["address"], "192.168.1.25")
        self.assertEqual(metadata["Ethernet"]["dns"], ["1.1.1.1", "8.8.8.8"])
        self.assertEqual(interfaces["Wi-Fi"]["status"], "inactive")
        self.assertEqual(metadata["WSL"]["kind"], "VM network")

    def test_parse_route_print(self):
        route = parse_route_print(WINDOWS_ROUTE_PRINT_SAMPLE)
        self.assertEqual(route["gateway"], "192.168.1.1")
        self.assertEqual(route["interface_ip"], "192.168.1.25")

    def test_parse_getmac_csv(self):
        metadata = parse_getmac_csv(WINDOWS_GETMAC_SAMPLE)
        self.assertEqual(metadata["Ethernet"]["hardware_mac"], "aa:bb:cc:dd:ee:ff")
        self.assertEqual(metadata["Wi-Fi"]["label"], "Intel(R) Wi-Fi 6 AX200 160MHz")

    def test_derive_subnet(self):
        subnet = derive_subnet(
            {
                "address": "172.17.6.246",
                "netmask": "255.255.254.0",
                "prefix": "23",
            }
        )
        self.assertEqual(subnet, "172.17.6.0/23")

    def test_parse_collect_result_linux_normalizes_groups(self):
        interfaces = parse_ip_addr_json(LINUX_IP_ADDR_JSON)
        default_route = parse_ip_route_json(LINUX_ROUTE_JSON)
        metadata = {}
        for name, info in interfaces.items():
            metadata[name] = {
                "label": name,
                "kind": "",
                "dns": ["1.1.1.1", "8.8.8.8"] if name == default_route["interface"] else [],
                "gateway": default_route["gateway"] if name == default_route["interface"] else "",
                "hardware_mac": info.get("mac", ""),
                "type_label": "",
            }
        data = parse_collect_result(interfaces, metadata, default_route)
        self.assertEqual(data["primary"]["iface"], "enp0s31f6")
        self.assertEqual(len(data["physical"]), 1)
        self.assertEqual(len(data["tunnels"]), 1)
        self.assertTrue(any(row["iface"] == "docker0" for row in data["system"]))
        self.assertTrue(any(row["iface"] == "veth1234" for row in data["system"]))

    def test_parse_collect_result_windows_maps_primary_and_subnet(self):
        interfaces, metadata = parse_ipconfig_all(WINDOWS_IPCONFIG_SAMPLE)
        route_info = parse_route_print(WINDOWS_ROUTE_PRINT_SAMPLE)
        default_route = {"gateway": route_info["gateway"], "interface": "Ethernet"}
        data = parse_collect_result(interfaces, metadata, default_route)
        self.assertEqual(data["primary"]["iface"], "Ethernet")
        ethernet = next(row for row in data["physical"] if row["iface"] == "Ethernet")
        self.assertEqual(ethernet["subnet"], "192.168.1.0/24")
        self.assertEqual(ethernet["dns"], ["1.1.1.1", "8.8.8.8"])

