def sample_row(
    iface: str,
    type_: str,
    state: str,
    ipv4: str,
    current_mac: str,
    *,
    subnet: str = "",
    primary: bool = False,
    gateway: str = "",
    dns: Optional[List[str]] = None,
    ipv6: str = "-",
    hardware_mac: Optional[str] = None,
    family: str = "",
) -> dict:
    return {
        "iface": iface,
        "label": iface,
        "type": type_,
        "family": family,
        "state": state,
        "primary": primary,
        "ipv4": ipv4,
        "ipv4_item": None,
        "ipv6": ipv6,
        "current_mac": current_mac,
        "hardware_mac": hardware_mac or current_mac,
        "gateway": gateway,
        "dns": dns or [],
        "netmask": "",
        "subnet": subnet,
        "private_mac": False,
    }


