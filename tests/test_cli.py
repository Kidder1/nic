    def test_parse_collect_result_keeps_lan_label_as_physical(self):
        interfaces = {
            "en6": {
                "name": "en6",
                "header": "UP",
                "status": "",
                "mac": "00:e0:4c:82:5a:b3",
                "ipv4": [{"address": "192.168.50.10", "netmask": "255.255.255.0", "prefix": "24"}],
                "ipv6": [],
            }
        }
        metadata = {
            "en6": {
                "label": "USB 10/100/1000 LAN",
                "kind": "Ethernet",
                "dns": [],
                "gateway": "",
                "hardware_mac": "00:e0:4c:82:5a:b3",
                "type_label": "USB 10/100/1000 LAN",
            }
        }
        data = parse_collect_result(interfaces, metadata, {"gateway": "", "interface": ""})
        self.assertEqual(len(data["physical"]), 1)
        self.assertEqual(data["physical"][0]["type"], "USB 10/100/1000 LAN")

